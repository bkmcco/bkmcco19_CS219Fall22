package Crypto;

public class Play {


    public static void main(String[] args) {

        System.out.println(-3/2);
        System.out.println(Math.floor(-1.25));
        System.out.println(Math.floor(-33.1));
        System.out.println(-5/2);
        System.out.println(Math.floor(-5/2));   //evaluates the argument first, then passes it to the function
        System.out.println(Math.floor(-5/2.0));
        System.out.println(Math.floorDiv(-3,2));    //python does floor division naturally


        //remainder play

        System.out.println(-15%12);
        System.out.println(-93%13);
        System.out.println(Math.floorMod(-15,12));
        System.out.println(Math.floorMod(-93,13));                             //always positive

    }



}
